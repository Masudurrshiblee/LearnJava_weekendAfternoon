package basic;

public class LearnEscapeSequence {

	public static void main(String[] args) {
		
		System.out.println("This is Qa learning class");
		System.out.println("This is Qa \tlearning class");
		System.out.println("This is Qa learning class\b1");
		System.out.println("This is Qa learning \nclass");
		System.out.println("This is Qa \'learning\' class");
		System.out.println("This is Qa \"learning\" class");
		System.out.println("This is Qa learning \\class");
		System.out.println("This is Qa \rlearning class");
		System.out.println("This is \fQa learning class");


	}

}
